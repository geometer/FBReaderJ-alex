package org.geometerplus.android.fbreader.fmanager;

import java.util.List;

import android.app.ProgressDialog;
import android.widget.ArrayAdapter;

public class ReturnRes implements Runnable {

	// **************************************************************************//
	// Enums //
	// **************************************************************************//

	// **************************************************************************//
	// Members //
	// **************************************************************************//
	private int myCurIdx = 0;
	
	private List<String> myOrders;
	
	private ArrayAdapter<String> myAdapter;
	
	private ProgressDialog myProgressDialog;
	
	// **************************************************************************//
	// Constructors //
	// **************************************************************************//
	public ReturnRes(List<String> orders, ArrayAdapter<String> adapter,
			ProgressDialog pd) {
		myOrders = orders;
		myAdapter = adapter;
		myProgressDialog = pd;
	}
	// **************************************************************************//
	// Getters //
	// **************************************************************************//

	// **************************************************************************//
	// Setters //
	// **************************************************************************//

	// **************************************************************************//
	// Publics //
	// **************************************************************************//
	public void refresh() {
		myCurIdx = 0;
		myOrders.clear();
		myAdapter.clear();
		myAdapter.notifyDataSetChanged();
	}
	
	public void run() {
		if (myOrders != null && myOrders.size() > 0) {
			myAdapter.notifyDataSetChanged();
			int i = myCurIdx;
			for (i = myCurIdx; i < myOrders.size(); i++) {
				myAdapter.add(myOrders.get(i));
			}
			myCurIdx = i;
		}
		myAdapter.notifyDataSetChanged();
		myProgressDialog.dismiss();
	}
	
	// **************************************************************************//
	// Abstracts //
	// **************************************************************************//

	// **************************************************************************//
	// Protected //
	// **************************************************************************//

	// **************************************************************************//
	// Privates //
	// **************************************************************************//

	// **************************************************************************//
	// Public Statics //
	// **************************************************************************//

	// **************************************************************************//
	// Private Statics //
	// **************************************************************************//

	// **************************************************************************//
	// Internal Classes //
	// **************************************************************************//

};
